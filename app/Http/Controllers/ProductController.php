     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        
	try {

		$paymentIntent = $request->user()->charge($request->price, $request->payment_id);
		if($paymentIntent->status == 'succeeded'){
			return redirect('/products')->with('message', 'Succesfully completed!');
		}else{
			return redirect('/products')->with('message', 'something wrong !');
		}

		} catch (\Exception $e) {

			return $e->getMessage();
		}
    }

